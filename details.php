				<h2>Espresso</h2>
				<p class="byline">Before we can describe the complex drinks that use espresso as their bases, we must understand what the espresso is!  While espressos are great mixed with milk, they are often enjoyed alone.  These are not for the faint of heart, though.</p>
			</header>

			<div class="row">
				<figure class="column">
					<img src="images/espresso.jpg" alt="A traditional espresso">
					<figcaption>Espressos are the most important aspect of all espresso-based drinks.</figcaption>
				</figure>

				<div class="column">
					<h3>When to Drink</h3>
					<p>Traditionally, espresso shots are drank after dinner.  Other times, they are thought of as quick shots to give you bolts of energy to continue your writing with little fuss.  Don't order an espresso if you like to nurse your drink for hours!  As you can tell by the picture, the espresso is very small.</p> 
				</div>

				<div class="column">
					<h3>How it is Made</h3>
					<p>An espresso is made by shooting hot (around 190 degrees fahrenheit) water through extremely finely ground coffee beans.  To be precise, one shot of espresso (which is one ounce or 30ml) is made with seven grams of ground coffee.  Quality espresso, made with the good beans, has a thick layer of crema atop.</p> 
				</div>
			</div>

			<div class="row">
				<figure class="column">
					<img src="images/espresso-var.jpg" alt="A luongo espresso">
					<figcaption>This luongo espresso has extra crema.</figcaption>
				</figure>

				<div class="column">
					<h3>Variations</h3>
					<p>A ristretto (restrained) espresso has less liquid, while a luongo (long) espresso has more liquid.  The ristretto is more intense in flavor and more concentrated than an espresso is.  Meanwhile, the luongo is more bitter and more dilute.  As pictured, sometimes it has more crema too!</p>
				</div>

					<div class="column">
					<h3>Best Place on Campus</h3>
					<p>I'll hardly ever recommend Peet's Coffee for their espressos.  Their espressos are usually bitter and metallic.  Starbucks is probably your best bet if you want a shot of espresso, though Finger Lakes over at the Med Center sometimes has a very experienced barista onhand.</p>
				</div>
			</div>
		</section>

		<section>
			<header>
				<h2>Macchiato</h2>
				<p class="byline">No, this isn't the same macchiato they serve in Starbucks!  The traditional macchiato is actually a lot smaller and different from what you're imagining in your head.  Macchiatos are essentially a less bitter counterpart to the espresso shot (given the extra bit of foam they have).</p>
			</header>

			<div class="row">
				<figure class="column">
					<img src="images/macchiato.jpg" alt="A traditional macchiato">
					<figcaption>A traditional macchiato is essentially an espresso shot with a bit of foam.</figcaption>
				</figure>

				<div class="column">
					<h3>When to Drink</h3>
					<p>Given the similarity to espressos, all occasions that would warrant a shot of espresso would likewise warrant a macchiato.  If you're transitioning over from a milk lifestyle over to a coffee shot lifestyle, a macchiato will ease you in.  It's also a great afternoon pick-me-up.</p>
				</div>

				<div class="column">
					<h3>How it is Made</h3>
					<p>As seen in the photo, the macchiato is a shot of espresso, served in the traditional espresso cup.  The only difference is that it's served with a tiny amount of milk foam.  The espresso would be prepared the standard way, and the barista would dollop a small scoop of milk foam atop the espresso shot.</p> 
				</div>
			</div>

			<div class="row">
				<figure class="column">
					<img src="images/macchiato-var.jpg" alt="A modern macchiato">
					<figcaption>The modern day interpretation of a macchiato, popularized by Starbucks.</figcaption>
				</figure>

				<div class="column">
					<h3>Variations</h3>
					<p>You can request more milk foam atop your espresso shot.  And a popular variation of the macchiato can be found in coffee chains across the U.S., essentially lattes mixed with a shot of syrup, typically caramel.  The popular caramel macchiato at Starbucks is made that way!</p>
				</div>

				<div class="column">
					<h3>Best Place on Campus</h3>
					<p>Peet's Coffee does not serve macchiatos.  The Starbucks version of the macchiato will land you something entirely differently, as pictured.  But I'm sure if you went to Starbucks or Fingerlakes and requested a shot of espresso with a dollop of milk foam, the barista would comply.</p>
				</div>
			</div>
		</section>

		<section>
			<header>
				<h2>Americano</h2>
				<p class="byline">How odd... a patriotic espresso drink? This drink actually does have an American origin story:  during WW2, American soldiers missed their drip coffees, so the Americano was an adaptation of the espresso to imitate the traditional drip coffee.</p>
			</header>

			<div class="row">
				<figure class="column">
					<img src="images/americano.jpg" alt="A traditional Americano">
					<figcaption>Notice the distinctive layer of crema, evidence of an espresso base.</figcaption>
				</figure>

				<div class="column">
					<h3>When to Drink</h3>
					<p>If you're at a coffee shop that doesn't sell standard drip coffees but does sell Americanos, order an Americano!  Generally, if you like the espressos from a coffee shop, you'll like their Americanos as well.  As pictured, Americanos also have the distinctive layer of crema.</p>
				</div>

				<div class="column">
					<h3>How it is Made</h3>
					<p>Essentially, the Americano is a shot (or two) of espresso with a lot of hot water.  A strong espresso that might taste overpowering as a concentrated shot may become easier to taste when given that burst of hot water.  You want to use a 1:1 ratio of espresso to water for your standard espresso.</p> 
				</div>
			</div>

			<div class="row">
				<figure class="column">
					<img src="images/americano-var.jpg" alt="A luongo Americano">
					<figcaption>This luongo Americano uses a luongo espresso shot.</figcaption>
				</figure>

				<div class="column">
					<h3>Variations</h3>
					<p>The iced Americano is a popular drink, made from cold water instead of hot.  You could also make a luongo Americano, as pictured.  If you're tired and need just a little more energy to get those last few pages of your screenplay finished, you could get a red eye Americano, using drip coffee instead of hot water.</p>
				</div>

				<div class="column">
					<h3>Best Place on Campus</h3>
					<p>This is one category where Peet's isn't all bad; their iced Americanos are rather tasty.  Starbucks has a fine Americano if you like the taste of their espressos.  And if you're truly indifferent between a drip coffee and an Americano, Connections (near ITS) has delicious standard black coffees.</p>
				</div>
			</div>
		</section>

		<section>
			<header>
				<h2>Cappuccino</h2>
				<p class="byline">This is the classic coffee drink known worldwide.  Any coffee shop that makes espressos will make a cappuccino.  I've heard a barista-friend say that you can deem the quality of a coffeeshop by the quality of the cappuccino, and I've found this to be true.</p>
			</header>

			<div class="row">
				<figure class="column">
					<img src="images/cappuccino.jpg" alt="A traditional cappuccino">
					<figcaption>A classic cappuccino has equal parts espresso, steamed milk, and milk foam.</figcaption>
				</figure>

				<div class="column">
					<h3>When to Drink</h3>
					<p>Traditionally, cappuccinos are drank in the morning with breakfast.  They're appropriate for whenever you want some sweetness without the sugar, due to the high amount of milk foam.  If it's past 3PM, I'll get a decaf cappuccino.  I'm a little biased, but it seems the cappuccino is timeless!</p>
				</div>

				<div class="column">
					<h3>How it is Made</h3>
					<p>The cappuccino is made with two shots of espresso, followed by an equal amount of steamed milk.  The top of the cappuccino is covered in milk foam, sometimes artfully as pictured.  The cappuccino is smaller than the latte, but chains like Starbucks have popularized larger sizes.</p> 
				</div>
			</div>

			<div class="row">
				<figure class="column">
					<img src="images/cappuccino-var.jpg" alt="A dry cappuccino">
					<figcaption>This dry cappuccino has extra milk foam.</figcaption>
				</figure>

				<div class="column">
					<h3>Variations</h3>
					<p>A wet cappuccino has more steamed milk and less milk foam than a regular cappuccino.  It still doesn't have as much steamed milk as a latte, however.  Conversely, a dry cappuccino, as pictured, has less steamed milk and more milk foam than a regular cappuccino.</p>
				</div>

				<div class="column">
					<h3>Best Place on Campus</h3>
					<p>I've had more bad cappuccinos this year than I can count from Peet's, typically with no milk foam at all.  I haven't had a bad experience with Starbucks yet.  Fingerlakes is OK, but since they cater to the sleep-deprived Med Center population, they add many espresso shots.</p>
				</div>
			</div>
