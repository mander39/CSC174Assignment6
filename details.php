		<section>
			<header>
				<h2>Latte</h2>
				<p class="byline">This is the ultimate millennial drink, sometimes used insultingly by opposers to espresso drinks (but not by me).  A latte is not the same as a cappuccino!  It has more steamed milk and less milk foam than a cappuccino.</p>
			</header>

			<div class="row">
				<figure class="column">
					<img src="images/latte.jpg" alt="A traditional latte">
					<figcaption>Latte art is a notorious aspect of lattes.</figcaption>
				</figure>

				<div class="column">
					<h3>When to Drink</h3>
					<p>Since lattes are so heavy in milk, it may be nicer to have them earlier in the day.  I would not recommend them as afternoon pick-me-ups because they are so dense.  You may fall asleep writing (or thinking about writing) your novel if you have a hot latte after lunch!</p>
				</div>

				<div class="column">
					<h3>How it is Made</h3>
					<p>The latte is made with one part espresso to two parts steamed milk, topped with a thin layer of milk foam.  The thin layer of milk foam allows latte art, pictured, to occur under the hands of a skilled barista.  The latte is the largest espresso-based drink.</p> 
				</div>
			</div>

			<div class="row">
				<figure class="column">
					<img src="images/latte-var.jpg" alt="A caffe mocha latte">
					<figcaption>This caffe mocha latte is indistinguishable from a regular latte.</figcaption>
				</figure>

				<div class="column">
					<h3>Variations</h3>
					<p>Lattes are commonly mixed with a flavor shot or powdered flavor base.  The dominance of milk in a latte helps other flavors stand out.  One of my favorite variations is the caffe mocha, pictured, which is like a bitter hot chocolate.  It is also common to drink iced latte in the USA.</p>
				</div>

				<div class="column">
					<h3>Best Place on Campus</h3>
					<p>Starbucks is the winner in this category.  They consistently have the best lattes compared to others places on campus.  Peet's lattes are too metallic tasting.  Finger Lakes has a fine latte under the hands of an experienced barista, though again, it can be kind of bitter.</p>
